COLORS = {
    'T': (123, 36, 237),
    'I': (83, 171, 252),
    'O': (247, 242, 96),
    'S': (250, 22, 60),
    'Z': (2, 209, 12),
    'L': (240, 120, 29),
    'J': (242, 68, 219)
}

class Tetromino:
    def __init__(self, shape, x, y):
        self.name = shape
        self.rotations = SHAPES[shape]
        self.rotation = 0
        self.x = x
        self.y = y
        self.color = COLORS[shape]
    
    @property
    def cells(self):
        return [(self.x + dx, self.y + dy) for dx, dy in self.rotations[self.rotation]]
    
